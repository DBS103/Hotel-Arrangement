package com.dbs.pojo;

public class Outhistory {
	private int clientno;
	private int roomid;
	private int empno;
	private String cotime;
	private String cotext;
	public int getClientno() {
		return clientno;
	}
	public void setClientno(int clientno) {
		this.clientno = clientno;
	}
	public int getRoomid() {
		return roomid;
	}
	public void setRoomid(int roomid) {
		this.roomid = roomid;
	}
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public String getCotime() {
		return cotime;
	}
	public void setCotime(String cotime) {
		this.cotime = cotime;
	}
	public String getCotext() {
		return cotext;
	}
	public void setCotext(String cotext) {
		this.cotext = cotext;
	}


}
